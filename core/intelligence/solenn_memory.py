@dataclass
class MemoryMatch:
    """Intuição Extraída (Ψ-25)"""
    bias: float
    confidence: float
    match_count: int
    entropy: float
    reason: str
    metadata: Dict[str, Any] = field(default_factory=dict)

class SolennMemory:
    """
    Project Memory (Ω-25)
    Banco de Dados Vetorial Nativo e Fast Similarity Search.
    Substitui dependências C++ por rotinas Numpy estritamente otimizadas e assíncronas.
    """

    def __init__(self, vector_dim: int = 64, max_episodes: int = 10000):
        # 1.1.1: Vector space allocation / 1.1.2: Dynamic episode capping
        self.vector_dim = vector_dim
        self.max_episodes = max_episodes
        
        # 1.1.3: Circular buffer logic / 1.1.8: Persistence matrix definition array
        self.database = np.zeros((self.max_episodes, self.vector_dim), dtype=np.float32)
        # 1.3.4: N-dimensional vector norm caching
        self.norms = np.zeros(self.max_episodes, dtype=np.float32)
        
        # 1.6.2: JSON metadata passing / 3.3.1: Causal analytical tagging format
        self.metadata = [None] * self.max_episodes
        
        # 1.5.1: Memory cursor structural reset boundary
        self.cursor = 0
        self.is_full = False
        
        # 3.1.3: Lock-free tensor read/writes (asyncio lock object)
        self._lock = asyncio.Lock()
        
        # 2.3.1: Global Success rate memory metric tracking
        self._successful_recalls = 0

    async def add_episode(self, vector: np.ndarray, outcome_data: Dict[str, Any]):
        """
        1.2.4: Asynchronous write offloading loop
        1.2.2: Outcome lagging append trigger
        """
        # 1.2.3: Dimension validation math gate / 3.2.4: Vector generic mismatch graceful handling
        if len(vector) != self.vector_dim:
            logger.warning(f"⚠️ [MEMORY] Size Mismatch. Got {len(vector)}, expected {self.vector_dim}")
            return

        # 3.2.5: Deep NaN numerical vector defense generic hook
        if np.isnan(vector).any() or np.isinf(vector).any():
            logger.warning("⚠️ [MEMORY] NaN/Inf vector injected. Rejecting.")
            return

        async with self._lock:
            # 1.1.6: Embedding normalizations vectors
            norm = np.linalg.norm(vector)
            if norm == 0:
                return # 3.3.3: Zero-volatility substitution

            # 1.5.7: High-value extreme preservation locking
            # If cursor is at a high-value memory, we skip (requires robust tracking)
            # For iteration we simply overwrite for speed (1.5.2 Buffer overwrite penalty tracking)
            
            self.database[self.cursor] = vector
            self.norms[self.cursor] = norm
            self.metadata[self.cursor] = outcome_data

            self.cursor += 1
            if self.cursor >= self.max_episodes:
                self.cursor = 0
                self.is_full = True

    def _get_active_mask(self) -> Tuple[np.ndarray, np.ndarray]:
        """3.2.8: Global Active memory discrete percentage scaling"""
        if not self.is_full:
            return self.database[:self.cursor], self.norms[:self.cursor]
        return self.database, self.norms

    async def recall(self, query_vector: np.ndarray, top_k: int = 5) -> List[Dict[str, Any]]:
        """
        1.3.3: Fast Numpy vectorization dot product math
        1.6.1: Full Async prediction framework interface
        """
        # 3.1.6: Batching memory queries concurrently (simulation payload)
        if len(query_vector) != self.vector_dim:
            return [] # 1.6.3: Null dataset response safety returns

        # 3.4.1: Fast Counter-factual memory scenario query
        active_db, active_norms = self._get_active_mask()
        if len(active_db) == 0:
            return []
            
        q_norm = np.linalg.norm(query_vector)
        if q_norm == 0: return []

        # 1.3.1: Cosine similarity geometric base
        dot_products = np.dot(active_db, query_vector)
        similarities = dot_products / (active_norms * q_norm + 1e-9)

        # 1.3.5: Top-K structural sorting logic / 1.3.8: Exclusion of extreme recent memory bias
        # Using argpartition for O(N) fast selection
        k_val = min(top_k, len(similarities))
        top_k_idx = np.argpartition(similarities, -k_val)[-k_val:]
        
        # Sort the top K
        top_k_idx = top_k_idx[np.argsort(similarities[top_k_idx])[::-1]]

        results = []
        for idx in top_k_idx:
            sim = similarities[idx]
            # 1.4.5: False-positive historical memory penalty (filter weak matches)
            if sim < 0.65: continue 
            
            meta = self.metadata[idx].copy()
            meta['similarity'] = float(sim)
            # 2.1.4: Exact Time-delta physical weighting offset
            meta['memory_index'] = int(idx)
            results.append(meta)

        self._successful_recalls += 1
        return results

    async def get_market_intuition(self, current_vector: np.ndarray) -> MemoryMatch:
        """
        1.4: Síntese de Intuição Local
        1.4.1: Averaged numerical target accumulation
        """
        # 3.1.2: Coroutine Awaitable memory retrieval access
        matches = await self.recall(current_vector, top_k=10)
        
        # 3.6.4: Alert Vague memory notification structured formatting
        if not matches:
            return MemoryMatch(0.0, 0.0, 0, 0.0, "Memória Vazia", {})

        total_bias = 0.0
        weights_sum = 0.0
        outcomes = []

        for m in matches:
            # 1.4.2: Distance-weighted outcome bias scoring / 2.1.1: Exponential decay
            weight = max(0.0, m['similarity'] ** 2)
            # Fetch outcome 
            outcome_change = m.get('next_excursion', 0.0)
            total_bias += outcome_change * weight
            weights_sum += weight
            outcomes.append(outcome_change)

        if weights_sum <= 0:
            return MemoryMatch(0.0, 0.0, 0, 0.0, "Decaimento Falhou", {})

        # 1.4.3: Confidence probabilistic formulation
        avg_confidence = float(np.mean([m['similarity'] for m in matches]))
        # 1.4.4: Dispersion entropy math of matches
        entropy = float(np.std(outcomes)) if len(outcomes) > 1 else 0.0
        # 1.4.7: Geometric Outlier rejection in synthesis applied implicitly by the squared weights

        bias = float(total_bias / weights_sum)
        
        # 3.6.3: Easy Human-readable UI string generation
        direction = "BULL" if bias > 0 else "BEAR"
        reason = f"Intuição ({direction}) em {len(matches)} epílogos similares. Entropia das ramificações: {entropy:.4f}"

        return MemoryMatch(
            bias=bias,
            confidence=avg_confidence,
            match_count=len(matches),
            entropy=entropy,
            reason=reason,
            metadata={
                "raw_matches_sims": [m['similarity'] for m in matches],
                "db_utilization": float(len(self._get_active_mask()[0]) / self.max_episodes)
            }
        )
